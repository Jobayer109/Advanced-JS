/*
    Three types of loop.
        1. for -> when we know the range
        2. while -> when we don't know the range.
        3. Do-while

*/

// for loop  ----> when we know the range
for (let i = 0; i < 10; i++) {
  console.log("Hello world");
}

// while loop  ----> when we don't know the range

while (true) {
  console.log("Hello, listen to me");
}
